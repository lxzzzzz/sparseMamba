import copy
import pickle
from pathlib import Path

import numpy as np
from skimage import io

from ...utils import box_utils, calibration_kitti, common_utils
from ..dataset import DatasetTemplate


class KittiTrackingFrameDataset(DatasetTemplate):
    """
    Frame-level dataset for detector inference on KITTI-tracking-style sequences.
    It consumes tracking info pickles generated by tools/create_tracking_infos.py.
    """

    def __init__(self, dataset_cfg, class_names, training=True, root_path=None, logger=None):
        super().__init__(
            dataset_cfg=dataset_cfg, class_names=class_names, training=training, root_path=root_path, logger=logger
        )
        self.split = self.dataset_cfg.DATA_SPLIT[self.mode]
        self.tracking_infos = []
        self.include_tracking_data(self.mode)
        self.class_name_remap = self.dataset_cfg.get('CLASS_NAME_REMAP', {})
        self.default_class_name = self.dataset_cfg.get('DEFAULT_CLASS_NAME', None)

    def _remap_names(self, names):
        if len(names) == 0:
            return names
        return np.asarray([
            self.class_name_remap.get(name, self.default_class_name if self.default_class_name is not None else name)
            for name in names
        ])

    def include_tracking_data(self, mode):
        if self.logger is not None:
            self.logger.info('Loading tracking frame dataset')

        infos = []
        for info_path in self.dataset_cfg.INFO_PATH[mode]:
            info_file = self.root_path / info_path
            if not info_file.exists():
                continue
            with open(info_file, 'rb') as f:
                infos.extend(pickle.load(f))

        self.tracking_infos.extend(infos)

        if self.logger is not None:
            self.logger.info('Total frames for tracking dataset: %d', len(infos))

    def __len__(self):
        if self._merge_all_iters_to_one_epoch:
            return len(self.tracking_infos) * self.total_epochs
        return len(self.tracking_infos)

    def get_lidar(self, info):
        lidar_path = self.root_path / info['point_cloud']['lidar_path']
        assert lidar_path.exists()
        return np.fromfile(str(lidar_path), dtype=np.float32).reshape(-1, info['point_cloud'].get('num_features', 4))

    def get_image(self, info):
        image_path = self.root_path / info['image']['image_path']
        assert image_path.exists()
        image = io.imread(image_path).astype(np.float32)
        return image / 255.0

    def get_calib(self, info):
        calib_dict = info['calib']
        return calibration_kitti.Calibration(calib_dict)

    @staticmethod
    def get_fov_flag(pts_rect, img_shape, calib):
        pts_img, pts_rect_depth = calib.rect_to_img(pts_rect)
        val_flag_1 = np.logical_and(pts_img[:, 0] >= 0, pts_img[:, 0] < img_shape[1])
        val_flag_2 = np.logical_and(pts_img[:, 1] >= 0, pts_img[:, 1] < img_shape[0])
        val_flag_merge = np.logical_and(val_flag_1, val_flag_2)
        return np.logical_and(val_flag_merge, pts_rect_depth >= 0)

    def __getitem__(self, index):
        if self._merge_all_iters_to_one_epoch:
            index = index % len(self.tracking_infos)

        info = copy.deepcopy(self.tracking_infos[index])
        calib = self.get_calib(info)
        img_shape = np.array(info['image']['image_shape'], dtype=np.int32)
        get_item_list = self.dataset_cfg.get('GET_ITEM_LIST', ['points'])

        input_dict = {
            'frame_id': str(info['frame_id']),
            'sequence_id': str(info['sequence_id']),
            'frame_idx': np.int32(info['frame_idx']),
            'calib': calib,
        }

        if 'annos' in info:
            annos = copy.deepcopy(info['annos'])
            annos = common_utils.drop_info_with_name(annos, name='DontCare')
            annos['name'] = self._remap_names(annos['name'])
            if len(annos['name']) > 0:
                gt_boxes_camera = np.concatenate(
                    [annos['location'], annos['dimensions'], annos['rotation_y'][..., np.newaxis]], axis=1
                ).astype(np.float32)
                gt_boxes_lidar = box_utils.boxes3d_kitti_camera_to_lidar(gt_boxes_camera, calib)
            else:
                gt_boxes_lidar = np.zeros((0, 7), dtype=np.float32)

            input_dict.update({
                'gt_names': annos['name'],
                'gt_boxes': gt_boxes_lidar,
            })

            if 'bbox' in annos and 'gt_boxes2d' in get_item_list:
                input_dict['gt_boxes2d'] = annos['bbox']

        if 'points' in get_item_list:
            points = self.get_lidar(info)
            if self.dataset_cfg.get('FOV_POINTS_ONLY', False):
                pts_rect = calib.lidar_to_rect(points[:, 0:3])
                fov_flag = self.get_fov_flag(pts_rect, img_shape, calib)
                points = points[fov_flag]
            input_dict['points'] = points

        if 'images' in get_item_list:
            input_dict['images'] = self.get_image(info)

        if 'calib_matrices' in get_item_list or 'calib_matricies' in get_item_list:
            p2 = np.eye(4, dtype=np.float32)
            p2[:3, :4] = calib.P2

            r0 = np.eye(4, dtype=np.float32)
            r0[:3, :3] = calib.R0

            v2c = np.eye(4, dtype=np.float32)
            v2c[:3, :4] = calib.V2C

            input_dict['trans_lidar_to_img'] = p2 @ r0 @ v2c
            input_dict['trans_lidar_to_cam'] = v2c
            input_dict['trans_cam_to_img'] = p2

        data_dict = self.prepare_data(data_dict=input_dict)
        data_dict['image_shape'] = img_shape
        return data_dict
